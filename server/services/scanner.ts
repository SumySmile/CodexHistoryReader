const SESSION_UPSERT_SQL = `
  INSERT INTO sessions (id, project_slug, project_path, summary, first_prompt, message_count,
    git_branch, model, created_at, modified_at, file_path, file_mtime,
    parent_session_id, is_subagent, codex_lineage_checked)
  VALUES (@id, @project_slug, @project_path, @summary, @first_prompt, @message_count,
    @git_branch, @model, @created_at, @modified_at, @file_path, @file_mtime,
    @parent_session_id, @is_subagent, @codex_lineage_checked)
  ON CONFLICT(id) DO UPDATE SET
    summary = COALESCE(@summary, summary),
    first_prompt = COALESCE(@first_prompt, first_prompt),
    message_count = MAX(COALESCE(@message_count, 0), COALESCE(message_count, 0)),
    git_branch = COALESCE(@git_branch, git_branch),
    model = COALESCE(@model, model),
    project_path = COALESCE(@project_path, project_path),
    created_at = COALESCE(created_at, @created_at),
    modified_at = COALESCE(@modified_at, modified_at),
    file_path = COALESCE(@file_path, file_path),
    file_mtime = COALESCE(@file_mtime, file_mtime),
    parent_session_id = COALESCE(@parent_session_id, parent_session_id),
    is_subagent = CASE
      WHEN COALESCE(@codex_lineage_checked, 0) = 1 THEN COALESCE(@is_subagent, 0)
      ELSE is_subagent
    END,
    codex_lineage_checked = MAX(COALESCE(codex_lineage_checked, 0), COALESCE(@codex_lineage_checked, 0)),
    indexed_at = CASE
      WHEN COALESCE(@file_path, '') <> COALESCE(file_path, '')
        OR COALESCE(@file_mtime, -1) <> COALESCE(file_mtime, -1)
        OR COALESCE(@modified_at, '') <> COALESCE(modified_at, '')
        OR COALESCE(@message_count, 0) <> COALESCE(message_count, 0)
      THEN NULL
      ELSE indexed_at
    END
`;

function isLowSignalPrompt(text: string | null | undefined): boolean {
  if (!text) return true;
  const t = text.trim();
  if (!t) return true;
  if (LOW_SIGNAL_PROMPT_RE.test(t)) return true;
  if (t.startsWith(CAVEAT_PREFIX)) return true;
  return false;
}

function extractClaudeTextFromContent(content: unknown): string | null {
  if (typeof content === 'string') return sanitizeConversationText(content);
  if (Array.isArray(content)) {
    const joined = content
      .filter((b: any) => b?.type === 'text' && typeof b?.text === 'string')
      .map((b: any) => b.text)
      .join('\n');
    return sanitizeConversationText(joined);
  }
  return null;
}

function extractCodexTextFromContent(content: unknown): string | null {
  if (!Array.isArray(content)) return null;
  const joined = content
    .filter((b: any) => (b?.type === 'input_text' || b?.type === 'output_text') && typeof b?.text === 'string')
    .map((b: any) => b.text)
    .join('\n');
  return sanitizeConversationText(joined);
}

function extractCopilotMessageText(message: any): string | null {
  if (!message) return null;
  if (typeof message.text === 'string') return sanitizeConversationText(message.text);
  if (Array.isArray(message.parts)) {
    const joined = message.parts
      .map((part: any) => typeof part?.text === 'string' ? part.text : '')
      .filter(Boolean)
      .join('\n');
    return sanitizeConversationText(joined);
  }
  return null;
}

function extractCopilotResponseText(response: any): string | null {
  if (!Array.isArray(response)) return null;
  const joined = response
    .map((item: any) => typeof item?.value === 'string' ? item.value : '')
    .filter(Boolean)
    .join('\n\n');
  return sanitizeConversationText(joined);
}

function cleanCodexPrompt(text: string | null | undefined): string | null {
  if (!text) return null;
  const cleaned = text
    .replace(/<environment_context>[\s\S]*?<\/environment_context>/gi, '')
    .replace(/^# Context from my IDE setup:[\s\S]*?## My request for Codex:\s*/i, '')
    .replace(/^# Context from my IDE setup:[\s\S]*?## My request:\s*/i, '')
    .trim();
  return sanitizeConversationText(cleaned);
}

function slugifyProjectPath(projectPath: string | null | undefined, fallback: string): string {
  if (!projectPath) return fallback;
  const slug = projectPath
    .replace(/[:\\/]/g, '-')
    .replace(/\s+/g, '-')
    .replace(/^-+|-+$/g, '');
  return slug || fallback;
}

function createSessionUpsertStatement() {
  return getDb().prepare(SESSION_UPSERT_SQL);
}

function yieldToEventLoop(): Promise<void> {
  return new Promise(resolve => setTimeout(resolve, 0));
}

export async function scanAllProjects(): Promise<number> {
  const upsert = createSessionUpsertStatement();

  let count = 0;
  count += await scanClaudeProjects(upsert);
  await yieldToEventLoop();
  count += await scanCodexSessions(upsert);
  await yieldToEventLoop();
  count += await scanVsCodeCopilotSessions(upsert);
  await yieldToEventLoop();
  count += await scanCursorCopilotSessions(upsert);
  return count;
}

export async function upsertSessionFromFile(filePath: string): Promise<string | null> {
  const normalized = filePath.replace(/[\\/]+/g, '/').toLowerCase();
  const upsert = createSessionUpsertStatement();
  const parentDir = path.dirname(filePath);

  if (
    path.basename(filePath) === 'state.vscdb'
    && normalized.includes('/appdata/roaming/cursor/user/workspacestorage/')
  ) {
    return upsertCursorWorkspaceStateSessionFile(upsert, filePath);
  }

  if (normalized.includes('/chatSessions/'.toLowerCase()) && path.extname(filePath).toLowerCase() === '.json') {
    return upsertVsCodeCopilotSessionFile(upsert, filePath);
  }

  if (normalized.includes('/.cursor/projects/') && normalized.includes('/agent-transcripts/')) {
    return upsertCursorTranscriptSessionFile(upsert, filePath);
  }

  if (
    normalized.includes('/.codex/sessions/')
    && path.extname(filePath).toLowerCase() === '.jsonl'
    && path.basename(filePath).startsWith('rollout-')
  ) {
    return upsertCodexSessionFile(upsert, filePath);
  }

  if (
    normalized.includes('/.claude/projects/')
    && path.extname(filePath).toLowerCase() === '.jsonl'
    && path.resolve(path.dirname(parentDir)) === path.resolve(PROJECTS_DIR)
  ) {
    return upsertClaudeSessionFile(upsert, filePath);
  }

  return null;
}

