async function upsertCursorTranscriptSessionFile(upsert: any, filePath: string): Promise<string | null> {
  if (!fs.existsSync(filePath)) return null;

  const db = getDb();
  const composerId = path.basename(filePath, '.jsonl');
  const metadata = buildCursorComposerMetadataIndex().get(composerId) || null;
  const conversation = readCursorComposerConversation(composerId);
  const transcriptMeta = await parseCursorTranscriptMeta(filePath);
  const stat = fs.statSync(filePath);
  const sessionId = `${COPILOT_ID_PREFIX}cursor-${composerId}`;
  const summary = metadata?.name || metadata?.subtitle || null;
  const firstPrompt = conversation.bubbles
    .find(bubble => bubble.role === 'user' && bubble.text && !isLowSignalPrompt(bubble.text))
    ?.text || transcriptMeta.firstPrompt;
  const createdAt = conversation.bubbles[0]?.createdAt
    || (metadata?.createdAt ? new Date(metadata.createdAt).toISOString() : transcriptMeta.createdAt);
  const modifiedAt = conversation.bubbles[conversation.bubbles.length - 1]?.createdAt
    || (metadata?.lastUpdatedAt ? new Date(metadata.lastUpdatedAt).toISOString() : transcriptMeta.modifiedAt);
  const messageCount = conversation.bubbles.length > 0
    ? conversation.bubbles.reduce((sum, bubble) => sum + countCursorBubbleMessages(bubble), 0)
    : transcriptMeta.messageCount;
  const projectDirName = path.basename(path.dirname(path.dirname(filePath)));

  if (metadata?.workspaceId) {
    db.prepare('DELETE FROM sessions WHERE id = ?').run(`${COPILOT_ID_PREFIX}cursor-${metadata.workspaceId}`);
    db.prepare('DELETE FROM sessions WHERE id = ?').run(`${COPILOT_ID_PREFIX}cursor-${metadata.workspaceId}-${composerId}`);
  }

  upsert.run({
    id: sessionId,
    project_slug: slugifyProjectPath(metadata?.workspacePath || projectDirName, 'copilot-cursor-unknown'),
    project_path: metadata?.workspacePath || null,
    summary: summary && summary !== firstPrompt ? summary.slice(0, 500) : null,
    first_prompt: firstPrompt?.slice(0, 500) || null,
    message_count: messageCount,
    git_branch: metadata?.activeBranch || metadata?.cachedGitBranch || metadata?.createdOnBranch || null,
    model: null,
    created_at: createdAt || new Date(stat.mtimeMs).toISOString(),
    modified_at: modifiedAt || new Date(stat.mtimeMs).toISOString(),
    file_path: filePath,
    file_mtime: stat.mtimeMs,
    parent_session_id: null,
    is_subagent: 0,
    codex_lineage_checked: 0,
  });

  return sessionId;
}

async function upsertCursorWorkspaceStateSessionFile(upsert: any, stateDbPath: string): Promise<string | null> {
  if (!fs.existsSync(stateDbPath)) return null;

  const db = getDb();
  const stat = fs.statSync(stateDbPath);
  const state = readCursorWorkspaceState(stateDbPath);
  if (!state) return null;

  const sessionId = `${COPILOT_ID_PREFIX}cursor-${state.workspaceId}`;
  const promptTexts = state.prompts
    .map(prompt => sanitizeConversationText(prompt.text))
    .filter((value): value is string => Boolean(value));
  const composerSummary = state.composers
    .slice()
    .sort((a, b) => (b.lastUpdatedAt || b.createdAt || 0) - (a.lastUpdatedAt || a.createdAt || 0))
    .map(composer => sanitizeConversationText(composer.name || composer.subtitle || null))
    .find((value): value is string => Boolean(value)) || null;
  const firstPrompt = promptTexts.find(text => !isLowSignalPrompt(text)) || null;
  const generationTimes = state.generations
    .map(entry => typeof entry.unixMs === 'number' ? new Date(entry.unixMs).toISOString() : null)
    .filter((value): value is string => Boolean(value));

  if (promptTexts.length === 0 && generationTimes.length === 0 && !composerSummary) return null;

  const duplicateComposer = state.workspacePath
    ? db.prepare(`
      SELECT id FROM sessions
      WHERE id <> ?
        AND id LIKE 'copilot-cursor-%'
        AND project_path = ?
        AND file_path <> ?
      LIMIT 1
    `).get(sessionId, state.workspacePath, stateDbPath) as { id: string } | undefined
    : undefined;

  if (duplicateComposer) {
    db.prepare('DELETE FROM sessions WHERE id = ?').run(sessionId);
    return null;
  }

  upsert.run({
    id: sessionId,
    project_slug: slugifyProjectPath(state.workspacePath, 'copilot-cursor-unknown'),
    project_path: state.workspacePath,
    summary: composerSummary && composerSummary !== firstPrompt ? composerSummary.slice(0, 500) : null,
    first_prompt: firstPrompt?.slice(0, 500) || null,
    message_count: state.prompts.length + state.generations.length,
    git_branch: state.cachedGitBranch,
    model: null,
    created_at: generationTimes[0] || new Date(stat.mtimeMs).toISOString(),
    modified_at: generationTimes[generationTimes.length - 1] || new Date(stat.mtimeMs).toISOString(),
    file_path: stateDbPath,
    file_mtime: stat.mtimeMs,
    parent_session_id: null,
    is_subagent: 0,
    codex_lineage_checked: 0,
  });

  return sessionId;
}

async function scanClaudeProjects(upsert: any): Promise<number> {
  const db = getDb();
  let count = 0;

  if (!fs.existsSync(PROJECTS_DIR)) return 0;

  const projectDirs = fs.readdirSync(PROJECTS_DIR, { withFileTypes: true })
    .filter(d => d.isDirectory())
    .map(d => d.name);

  for (const slug of projectDirs) {
    const projectDir = path.join(PROJECTS_DIR, slug);
    const indexPath = path.join(projectDir, 'sessions-index.json');

    if (fs.existsSync(indexPath)) {
      try {
        const indexData = JSON.parse(fs.readFileSync(indexPath, 'utf-8'));
        const entries: any[] = indexData.entries || [];
        for (const entry of entries) {
          if (entry.isSidechain) continue;
          const filePath = entry.fullPath || path.join(projectDir, `${entry.sessionId}.jsonl`);
          let summary = sanitizeConversationText(entry.summary);
          let firstPrompt = sanitizeConversationText(entry.firstPrompt);
          if (!summary && isLowSignalPrompt(firstPrompt)) {
            const meta = await parseClaudeFirstLines(filePath);
            if (!isLowSignalPrompt(meta.firstPrompt)) {
              firstPrompt = meta.firstPrompt!;
            }
          }
          upsert.run({
            id: entry.sessionId,
            project_slug: slug,
            project_path: entry.projectPath || null,
            summary,
            first_prompt: (!isLowSignalPrompt(firstPrompt) ? firstPrompt : null)?.slice(0, 500) || null,
            message_count: entry.messageCount || 0,
            git_branch: entry.gitBranch || null,
            model: null,
            created_at: entry.created || null,
            modified_at: entry.modified || entry.lastModified || null,
            file_path: filePath,
            file_mtime: entry.fileMtime || null,
            parent_session_id: null,
            is_subagent: 0,
            codex_lineage_checked: 0,
          });
          count++;
        }
      } catch (e) {
        console.error(`Failed to parse index for ${slug}:`, e);
      }
    }

    const jsonlFiles = fs.readdirSync(projectDir).filter(f => f.endsWith('.jsonl'));
    for (const file of jsonlFiles) {
      const sessionId = path.basename(file, '.jsonl');
      const existing = db.prepare('SELECT file_mtime FROM sessions WHERE id = ?').get(sessionId) as any;
      const filePath = path.join(projectDir, file);
      const stat = fs.statSync(filePath);
      if (existing && existing.file_mtime >= stat.mtimeMs) continue;
      if (existing) continue;

      try {
        const meta = await parseClaudeFirstLines(filePath);
        upsert.run({
          id: sessionId,
          project_slug: slug,
          project_path: null,
          summary: null,
          first_prompt: sanitizeConversationText(meta.firstPrompt)?.slice(0, 500) || null,
          message_count: 0,
          git_branch: meta.gitBranch || null,
          model: meta.model || null,
          created_at: meta.timestamp || null,
          modified_at: new Date(stat.mtimeMs).toISOString(),
          file_path: filePath,
          file_mtime: stat.mtimeMs,
          parent_session_id: null,
          is_subagent: 0,
          codex_lineage_checked: 0,
        });
        count++;
      } catch (e) {
        console.warn('[scanner] Skip file', filePath, ':', (e as Error).message);
      }
    }
  }

  return count;
}

async function scanCodexSessions(upsert: any): Promise<number> {
  const db = getDb();
  let count = 0;

  if (!fs.existsSync(CODEX_SESSIONS_DIR)) return 0;

  const indexMap = readCodexSessionIndex();
  const files = listFiles(CODEX_SESSIONS_DIR, filePath => filePath.endsWith('.jsonl'));

  for (const filePath of files) {
    try {
      const meta = await parseCodexFirstLines(filePath);
      if (!meta.sessionId) continue;

      const sessionId = `${CODEX_ID_PREFIX}${meta.sessionId}`;
      const stat = fs.statSync(filePath);
      const existing = db.prepare('SELECT file_mtime, codex_lineage_checked FROM sessions WHERE id = ?').get(sessionId) as any;
      if (existing && existing.file_mtime >= stat.mtimeMs && existing.codex_lineage_checked === 1) continue;

      const indexEntry = indexMap.get(meta.sessionId);
      const summary = sanitizeConversationText(indexEntry?.thread_name || null);
      const projectPath = meta.cwd || null;

      upsert.run({
        id: sessionId,
        project_slug: slugifyProjectPath(projectPath, 'codex-unknown'),
        project_path: projectPath,
        summary,
        first_prompt: (!isLowSignalPrompt(meta.firstPrompt) ? meta.firstPrompt : null)?.slice(0, 500) || null,
        message_count: 0,
        git_branch: null,
        model: meta.model || null,
        created_at: meta.timestamp || null,
        modified_at: indexEntry?.updated_at || new Date(stat.mtimeMs).toISOString(),
        file_path: filePath,
        file_mtime: stat.mtimeMs,
        parent_session_id: meta.parentSessionId || null,
        is_subagent: meta.isSubagent ? 1 : 0,
        codex_lineage_checked: 1,
      });
      count++;
    } catch (e) {
      console.warn('[scanner] Skip Codex session', filePath, ':', (e as Error).message);
    }
  }

  return count;
}

